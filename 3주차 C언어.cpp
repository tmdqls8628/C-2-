#include <stdio.h>

int main() {
    int N;
    printf("상품 종류 수를 입력하세요: ");
    scanf("%d", &N);

    int stock[100] = { 0 };
    int sold[100] = { 0 };
    int result[100] = { 0 };

    printf("%d개의 상품에 대한 입고 수량을 입력하세요:\n", N);
    for (int i = 0; i < N; i++) {
        printf("상품 %d 입고 수량: ", i + 1);
        scanf("%d", &stock[i]);
    }

    printf("%d개의 상품에 대한 판매 수량을 입력하세요:\n", N);
    for (int i = 0; i < N; i++) {
        printf("상품 %d 판매 수량: ", i + 1);
        scanf("%d", &sold[i]);
    }

    for (int i = 0; i < N; i++) {
        result[i] = stock[i] - sold[i];
    }

    int id;
    printf("재고를 확인할 상품 ID를 입력하세요 (1 ~ %d): ", N);
    scanf("%d", &id);

    printf("상품 %d의 현재 재고 수량: %d\n", id, result[id - 1]);

    printf("모든 상품의 재고 수량:\n");
    for (int i = 0; i < N; i++) {
        printf("%d", result[i]);
        if (i != N - 1) printf(" ");
    }
    printf("\n");

    return 0;
}
